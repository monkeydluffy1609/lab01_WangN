// This program shows the ranges of different primitive types.

public class DataTypes
{
	public static void main(String args[])
	{
		int a;
		a = Integer.MAX_VALUE;
		System.out.println("Maximum value of type integer is " + a);
		
		System.out.println("Miniimum value of type byte is " + Byte.MIN_VALUE);
		System.out.println("Minimum value of type short is " + Short.MIN_VALUE);
		System.out.println("Minimum value of type long is " + Long.MIN_VALUE);
		System.out.println("Minimum value of type float is " + Float.MIN_VALUE);
		System.out.println("Minimum value of type double is " + Double.MIN_VALUE);
				
	}
}